    - Press `F1` and type `MS SQL: Connect`.
    - Enter your server details (server name, authentication type, username, password, database).

3. **Create and run SQL files**:
    - Open or create a `.sql` file.
    - Write your SQL code (like your `USE SalesDB` statement).
    - Right-click in the editor and select "Run Query" or press `Ctrl+Shift+E`.

This will allow you to run SQL queries directly from VS Code.